<template>
  <div
    class="cursor-pointer transition-all duration-150 ease-in-out"
    :class="{
      'bg-yellow-100 rounded-md px-3 -mx-3': highlight,
    }"
    @click="toggleHighlight"
  >
    <p ref="el">
      <slot />
    </p>
  </div>
</template>

<script setup>
// Get a "hash" of the content
const slots = useSlots();
const hash = computed(() => {
  return (
    slots
      // Get the default slot
      .default()

      // Get the text from each node
      .map((slot) => {
        return slot.children;
      })
      .filter((text) => typeof text === 'string')

      // Combine the first letters of each word
      .map((text) =>
        text
          .split(' ')
          .map((word) => word[0])
          .join('')
      )
      .join('')
  );
});

const route = useRoute();
const highlight = ref(false);
const hide = ref(false);
const el = ref(null);

// We need to rely entirely on side-effects here because
// the hash doesn't exist on the server
watch(
  () => route.hash,
  () => {
    checkHash();
  }
);
onMounted(() => {
  checkHash();

  // Scroll to the highlighted element
  if (highlight.value && el.value) {
    el.value.scrollIntoView({ behavior: 'smooth' });
  }
});

const checkHash = () => {
  const isHighlighted = (highlight.value =
    route.hash === `#${hash.value}`);

  if (!isHighlighted && route.hash) {
    hide.value = true;
  } else {
    hide.value = false;
  }

  highlight.value = isHighlighted;
};

async function toggleHighlight(event) {
  // Make sure we clicked on a paragraph element
  if (event.target.tagName !== 'P') {
    return;
  }

  if (hash.value === '') {
    return;
  }

  if (highlight.value) {
    await navigateTo({ hash: '' }, { replace: true });
  } else {
    await navigateTo(
      { hash: `#${hash.value}` },
      { replace: true }
    );
  }

  checkHash();
}
</script>
